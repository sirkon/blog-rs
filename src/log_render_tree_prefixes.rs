#[inline(always)]
pub(crate) fn render_tree_prefix(dst: &mut Vec<u8>, mut bitmap: u64, mut depth: isize) {
    while depth > 0 {
        let mask = bitmap & 0xFF;
        let mut prefix = PREFIXES[mask as usize];

        if depth < 8 {
            prefix = &prefix[..(u64::count_ones(mask) * 2 + 3 * depth as u32) as usize];
        }

        dst.extend_from_slice(prefix.as_bytes());

        bitmap >>= 8;
        depth -= 8;
    }
}

