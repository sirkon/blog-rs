#[cfg(test)]
mod test {
    use crate::log_render_tree_prefixes::render_tree_prefix;

    #[test]
    fn test_prefixes() {
        #[derive(Copy, Clone, Debug)]
        struct TestSample {
            stack: &'static [bool],
            want:  &'static str,
        }

        let tests = [
            TestSample {
                stack: &[],
                want:  "",
            },
            TestSample {
                stack: &[true],
                want:  "тФВ  ",
            },
            TestSample {
                stack: &[false],
                want:  "   ",
            },
            TestSample {
                stack: &[true, true, false],
                want:  "тФВ  тФВ     ",
            },
            TestSample {
                stack: &[false, true, true],
                want:  "   тФВ  тФВ  ",
            },
            TestSample {
                stack: &[true, false, false, false, false, false, false, false, false],
                want:  "тФВ                          ",
            },
            TestSample {
                stack: &[true, true],
                want: "тФВ  тФВ  ",
            },
        ];

        let mut tmp_name = String::new();
        for t in &tests {
            let depth = t.stack.len();
            let name: &str = match depth {
                0 => "empty",
                _ => {
                    tmp_name = format!("{}", depth);
                    for b in t.stack.iter() {
                        tmp_name += ".";
                        if *b {
                            tmp_name += "1"
                        } else {
                            tmp_name += "0"
                        }
                    }
                    tmp_name.as_str()
                }
            };

            if depth > 64 {
                panic!("{}: depths over 64 are not supported, got {}", name, depth);
            }

            let mut bitmap: u64 = 0;
            for (i, b) in t.stack.iter().enumerate() {
                if *b {
                    bitmap |= 1 << i;
                }
            }

            let mut pref: Vec<u8> = Vec::with_capacity(depth * 5);
            render_tree_prefix(&mut pref, bitmap, depth as isize);
            unsafe {
                let got = str::from_utf8_unchecked(&pref);
                if got != t.want {
                    panic!(
                        "{} data mismatch:\nwant: ^{}$\ngot:  ^{}$",
                        name, t.want, got
                    )
                }
            }
        }
    }
}
